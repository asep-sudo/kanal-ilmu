<script>
document.addEventListener("DOMContentLoaded", function () {
    const faqItems = document.querySelectorAll(".faq-item");

    faqItems.forEach(item => {
        const btn = item.querySelector(".faq-question");

        btn.addEventListener("click", () => {
            faqItems.forEach(i => {
                if (i !== item) i.classList.remove("active");
            });

            item.classList.toggle("active");
        });
    });
});
</script>

<!-- FAQ Tab Group -->
<script>
document.addEventListener("DOMContentLoaded", () => {
    const tabs = document.querySelectorAll(".faq-tab");
    const groups = document.querySelectorAll(".faq-group");

    tabs.forEach(tab => {
        tab.addEventListener("click", () => {
            const targetId = tab.dataset.target;
            const target = document.getElementById(targetId);

            // Debug jika target tidak ketemu
            if (!target) {
                console.error("FAQ target tidak ditemukan:", targetId);
                return;
            }

            tabs.forEach(t => t.classList.remove("active"));
            groups.forEach(g => g.classList.remove("active"));

            tab.classList.add("active");
            target.classList.add("active");
        });
    });
});
</script>

<script>
document.addEventListener("DOMContentLoaded", () => {
    const searchInput = document.getElementById("faqSearch");

    searchInput.addEventListener("input", () => {
        const keyword = searchInput.value.toLowerCase();

        // hanya cari di FAQ group yang aktif
        const activeGroup = document.querySelector(".faq-group.active");
        if (!activeGroup) return;

        const items = activeGroup.querySelectorAll(".faq-item");

        items.forEach(item => {
            const text = item.innerText.toLowerCase();
            item.style.display = text.includes(keyword) ? "" : "none";
        });
    });
});
</script>

